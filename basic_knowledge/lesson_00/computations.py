#float() - input jest liczbą zmiennoprzecinkową
a = float(input('Enter the first number '))
b = float(input('Enter the second number '))

print('The sum of the numbers is ', a + b)
print('The difference in numbers is ', a - b)
print('The product of the numbers is ', a * b)
print('The quotient of numbers is ', a / b)
print('The remainder of the division is ', a % b)
print('The total part of the division is ', a // b)
print('The power is ', a ** b)


